/**
 * 文件信息
 * */
class FileInfo {
    /**
     * Id
     */
    Id: string;

    /**
     * 名称
     */
    Name: string;

    /**
     * 名称
     */
    FileType: string;

    /**
     * 内容类型
     */
    ContentType: string;

    /**
     * 文件扩展名
     */
    Extension: string;

    /**
     * 文件类型
     */
    FullName: string;

    /**
     * MD5校验值
     */
    MD5: string;

    /**
     * 服务器标识
     */
    ServerKey: string;

    /**
     * 存储类型
     */
    StorageType: string;

    /**
     * 文件大小
     */
    Size: string;

    /**
     * 状态
     */
    State: string;

    /**
     * 创建者名称
     */
    CreatorName: string;

    /**
     * 创建时间
     */
    CreateTime: Date;
}